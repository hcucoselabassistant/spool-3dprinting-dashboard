// Concatenates supabase/migrations/*.sql into one file for pasting into the
// Supabase SQL editor, for when the project lives on an account the CLI cannot
// manage.
//
// The output (supabase/bundle.sql) is committed for convenience, but it is
// GENERATED -- never edit it by hand. Edit the migrations and re-run
// `npm run db:bundle`. supabase/migrations/ stays the single source of truth.

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const MIGRATIONS_DIR = "supabase/migrations";
const OUT = "supabase/bundle.sql";

const files = readdirSync(MIGRATIONS_DIR)
  .filter((f) => f.endsWith(".sql"))
  .sort();

if (files.length === 0) {
  console.error(`No .sql files found in ${MIGRATIONS_DIR}`);
  process.exit(1);
}

const parts = [
  "-- GENERATED by `npm run db:bundle` -- do not edit by hand.",
  "-- Source of truth is supabase/migrations/.",
  "--",
  "-- Paste this whole file into the Supabase SQL editor and run it once.",
  "-- It is not idempotent: running it twice will fail on the create type",
  "-- statements. That is intentional -- it should fail loudly rather than",
  "-- half-apply.",
  "",
  "begin;",
  "",
];

for (const file of files) {
  parts.push(
    `-- ${"=".repeat(68)}`,
    `-- ${file}`,
    `-- ${"=".repeat(68)}`,
    "",
    readFileSync(join(MIGRATIONS_DIR, file), "utf8").trimEnd(),
    "",
  );
}

parts.push("commit;", "");

writeFileSync(OUT, parts.join("\n"));
console.log(`Wrote ${OUT} from ${files.length} migrations:`);
for (const f of files) console.log(`  ${f}`);
